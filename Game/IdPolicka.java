package Game;

public class IdPolicka {
	
	private int horizontalne;
	private int vertikalne;
	
	public IdPolicka(int horizontalne, int vertikalne) {
	
		this.horizontalne = horizontalne;
		this.vertikalne = vertikalne;
		
	}


	public int getHorizontalne() {
		return horizontalne;
	}

	public void setHorizontalne(int horizontalne) {
		this.horizontalne = horizontalne;
	}

	public int getVertikalne() {
		return vertikalne;
	}

	public void setVertikalne(int vertikalne) {
		this.vertikalne = vertikalne;
	}
}